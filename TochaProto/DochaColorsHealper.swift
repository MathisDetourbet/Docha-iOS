import Foundation

extension UIColor {
    class func blueDochaColor() -> UIColor {
        return UIColor(red: 0.28, green: 0.65, blue: 1.00, alpha: 1.0)
    }
    
    class func redDochaColor() -> UIColor {
        return UIColor(red: 0.99, green: 0.35, blue: 0.31, alpha: 1.0)
    }
    
    class func darkBlueDochaColor() -> UIColor {
        return UIColor(red:0.11, green:0.18, blue:0.24, alpha:1.0)
    }
    
    class func lightGrayDochaColor() -> UIColor {
        return UIColor(red:0.94, green:0.94, blue:0.96, alpha:1.0)
    }
    
    class func greenDochaColor() -> UIColor {
        return UIColor(red:0.27, green:0.91, blue:0.64, alpha:1.0)
    }
}
